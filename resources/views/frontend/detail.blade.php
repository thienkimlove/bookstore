                            <div id="book_review_summary">
                                <p>We are in the midst of a revolution. It is a scientific
                                    revolution built upon the tools of molecular biology, with which we
                                    probe and prod the living world in ways unimaginable a few decades ago.
                                    Need to track a bacterium at the root of a hospital outbreak? No
                                    problem: the offending germ's complete genetic profile can be obtained
                                    in 24 hours. We insert human DNA into E. coli bacteria to produce our
                                    insulin.</p>
                                <p>It is natural to look at biotechnology in the 21st century with a mix
                                    of wonder and fear. But biotechnology is not as 'unnatural' as one
                                    might think. All living organisms use the same molecular processes to
                                    replicate their genetic material and the same basic code to 'read' their
                                    genes. The similarities can be seen in their DNA. Here, John Archibald
                                    shows how evolution has been 'plugging-and-playing' with the subcellular
                                    components of life from the very beginning and continues to do so
                                    today. For evidence, we need look no further than the inner workings of
                                    our own cells. Molecular biology has allowed us to gaze back more than
                                    three billion years, revealing the microbial mergers and acquisitions
                                    that underpin the development of complex life. One Plus One Equals One
                                    tells the story of how we have come to this realization and its
                                    implications.</p>
